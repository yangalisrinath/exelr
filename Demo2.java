public class GreatestOfTwo {
    public static void main(String[] args) {
        int number1 = 10;
        int number2 = 20;

        if (number1 > number2) {
            System.out.println("The greatest number is " + number1);
        } else {
            System.out.println("The greatest number is " + number2);
        }
    }
}
